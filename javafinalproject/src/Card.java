
public class Card {
    private int value;
    public Card(){
        
    }
    
    public void add(int x){
        
        value+=x;    
    }
    
    public void subtract(int x){
        if(x<=value)
        value-=x;
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
}
